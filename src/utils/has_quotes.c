#include "../../include/minishell.h"

int	has_quotes(char	*token)
{
	size_t	i;

	i = 0;
	while (token[i])
	{
		if (token[i] == '\'' || token[i] == '\"')
			return (1);
		i++;
	}
	return (0);
}
